import { loginByUsername } from '@/api/login'
import LangSelect from '@/components/LangSelect'
import SocialSign from './socialsignin'

// eslint-disable-next-line no-unused-vars
var _that
export default {
  name: 'Login',
  components: { LangSelect, SocialSign },
  data() {
    return {
      loginForm: {
        username: '',
        password: '',
        country: '1'
      },
      passwordType: 'password',
      loading: false,
      showDialog: false,
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }

  },
  created() {
    // window.addEventListener('hashchange', this.afterQRScan)
  },
  destroyed() {
    // window.removeEventListener('hashchange', this.afterQRScan)
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
    },
    handleLogin() {
      if (this.loginForm.country === '1') {
        this.$store.dispatch('getuseCountry', 1)
      }
      if (this.loginForm.country === '2') {
        this.$store.dispatch('getuseCountry', 2)
      }
      this.loading = true
      loginByUsername(this.loginForm.username, this.loginForm.password)
        .then(res => {
          if (res.data.ret === 100) {
            this.$message({
              showClose: true,
              message: res.data.msg,
              type: 'error'
            })
            this.loading = false
          } else if (res.data.ret === 200) {
            this.$message({
              showClose: true,
              message: 'login successful',
              type: 'success'
            })
            this.$store.dispatch('LoginByUsername', this.loginForm)
              .then(res => {
                this.loading = false
                this.$router.push({ path: this.redirect || '/' })
              })
              .catch(() => {
                this.loading = false
              })
          }
        })
        .catch(() => {
          this.loading = false
        })
    },
    afterQRScan() {
